public abstract class Forma{
	
	/**
	* Metodo abstrato que requer implementacao de calculo de perimetro
	*/
	public abstract float calcularPerimetro();

	/**
	* Metodo abstrato que requer implementacao de calculo de area
	*/
	public abstract float calcularArea();

	/**
	* Metodo abstrato que requer implementacao para impressao de area e perimetro
	*/
	public abstract void print();

}